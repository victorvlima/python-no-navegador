"""
📦 VARIÁVEIS - Guardando informações

Variáveis são como caixas que guardam dados
"""

# Criando variáveis
nome = "Ana"
idade = 25
altura = 1.65
estudante = True

print(f"Nome: {nome}")
print(f"Idade: {idade}")
print(f"Altura: {altura}m")
print(f"É estudante: {estudante}")

# Mudando valores
idade = 26
print(f"Nova idade: {idade}")

# Tipos de dados
numero_inteiro = 42        # int
numero_decimal = 3.14      # float
texto = "Python"           # str
verdadeiro_falso = False   # bool

print(f"Inteiro: {numero_inteiro}")
print(f"Decimal: {numero_decimal}")
print(f"Texto: {texto}")
print(f"Booleano: {verdadeiro_falso}")

